package com.example.localdb.Classes;

public class User{

    User user = new User();

    private String name;
    private int age;
    private String job;
    private String gender;




    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getJob() {
        return job;
    }

    public String getGender() {
        return gender;
    }



    public void setName() {
        this.name = name;
    }

    public void setAge() {
        this.age = age;
    }

    public void setJob() {
        this.job = job;
    }

    public void setGender() {
        this.gender = gender;
    }

}
